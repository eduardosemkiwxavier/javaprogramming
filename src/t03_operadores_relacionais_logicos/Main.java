package t03_operadores_relacionais_logicos;

public class Main {
    
    public static void main(String[] args) {
        
        // Operadores relacionais são usados em condições e loops
        System.out.println(1 > 2);          // Operador >  (maior que)
        System.out.println(2 >= 2);         // Operador >= (maior ou igual que)
        System.out.println(2 < 2);          // Operador <  (menor que)
        System.out.println(1 <= 2);         // Operador <= (menor ou igual que)
        System.out.println(1 == 2);         // Operador == (igual a)
        System.out.println(1 != 2);         // Operador != (diferente de)
        
        
        // Operadores lógicos são expressões booleanas para fazer comparações complexas
        System.out.println(true && true);    // Operador &&  (true E true = true)
        System.out.println(false || true);   // Operador &&  (true E true = true)
        
    }

}
